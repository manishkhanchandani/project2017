<p><strong>Attempt</strong></p>
<p>Attempt is a <strong>substantial step</strong> taken towards committing the <strong>intended crime</strong>. </p>
<p><strong>Substantial Step</strong></p>
<p>A substantial step is a <strong>ACT</strong> that brings the defendant <strong>DANGEROUSLY CLOSE</strong> to the <strong>Intended Crime</strong>. Here A made a Substantial Step towards commiting his intended crime because he came Dangerously close because he has &quot;matches&quot; and made severe tries to set the shed on fire. </p>
<p><strong>Intended Crime</strong></p>
<p>Intended crime is the crime to be committed. Here <strong>intended crime is Arson</strong> because Defendant wants to burn the shed on fire. Here A acted <strong>with Intent to commit arson</strong> because he decide to &quot;set fire&quot; to the atheletic equipment shed. </p>
<p>Therefore, D can be charged with Attempted ..... </p>
