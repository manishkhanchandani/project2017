<p><strong>What were the TERMS of the bargain? </strong></p>
<p>

Under the UCC the subject matter, quantity and parties must be specified for a contract to be sufficiently certain, but all other terms can be supplied by the UCC’s GAP FILLERS.</p>
<p>

Here the parties are Al and Bob and the subject matter is one tuxedo that is to be "exactly Al's size." Although price was not agreed upon that term would be inferred by the normal price for such goods. If $519.95 was the normal price of the tuxedo, it would be the price supplied by the UCC. The place of delivery was Bob's store and the time of delivery was agreed to be "later in the week."</p>
<p>

Therefore the TERMS of the contract were sufficiently certain for it to be enforced.
