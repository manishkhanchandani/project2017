<p><strong>Rule</strong></p>
<p>If an acceptance forms a contract, for common law say,</p>
<p>

Under common law enforceable contracts generally must indicate PARTIES, PRICE, SUBJECT MATTER, TIME OF PERFORMANCE and QUANTITY. Most express terms and promises are considered to be EXPRESS MATERIAL CONDITIONS, except for TIME of performance, buyer SATISFACTION, and promises NOT TO ASSIGN the contract which are generally considered mere “covenants”.</p>
<p>



If an acceptance forms a contract, for UCC say,</p>
<p>

Under the UCC a contract is enforceable if the QUANTITY and PARTIES are specified. Other terms will be provided by the GAP FILLER provisions of the UCC. All express terms and promises are treated as material terms under the PERFECT TENDER RULE.</p>
