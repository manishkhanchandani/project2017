<p><strong>TERMS?</strong></p>
<p>
Under UCC Article 2 a goods contract is enforceable if quantity and parties are specified.. All other terms can be supplied by the UCC’s GAP FILLERS.</p>
<p>

Here the parties are specified as Sellco and Buyco. The quantity is 10,000 yellow widgets at a price of $6 without a warranty for delivery on 6/8.</p>
