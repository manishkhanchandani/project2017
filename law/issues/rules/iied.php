<p><strong>Rule</strong></p>
<p>Under tort law IIED is an <strong>intentional, outrageous act</strong> by the defendant which causes the plaintiff <strong>severe emotional distress</strong>. </p>
<p><strong>Analysis</strong></p>
<p>Generally emotional distress will not be assumed, and the plaintiff must present evidence showing that they did, in fact, suffer severe emotional distress. Evidence of mere humiliation and embarrassment is generally not enough.    </p>
<p>Here there was an <strong>intentional, outrageous act</strong> because D "flew" P to the top of a building and strand him there in front of a crowd where he was &quot;taunted for hours&quot;.
    
    (or facts from hypo) </p>
<p>However there is <strong>no evidence</strong> P suffered <strong>severe emotional distress</strong> because the facts only state that he was "humiliated" and &quot;embarrassed&quot;. (or facts from hypo)</p>
<p>Therefore, D can only be liable for IIED if P presents additional evidence of emotional distress.</p>
<p><strong>Conclusion</strong></p>
<p>Therefore the defendant may be liable for intentional infliction. </p>
