<p><strong>Rule</strong></p>
<p>Under contract law only <strong>good faith agreements are enforceable</strong>. No contract agreement induced by <strong>illegal threats</strong> is valid. Likewise, agreements induced by deliberate <strong>acts to create threats of economic harm are not enforceable</strong>. </p>
