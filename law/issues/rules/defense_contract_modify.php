<p><strong>Rule</strong></p>
<p>Under UCC 2-209 contract <strong>modifications</strong> do <strong>not require consideration</strong>, but if a contract as modified would have to be <strong>supported by a sufficient writing</strong> under UCC 2-201, and the <strong>modification is not supported by a writing</strong>, it is <strong>not enforceable</strong> at law. </p>
