<p><strong>Arson Example</strong></p>
<p>Under common law, Arson is <strong>MALICIOUS BURNING</strong> of the <strong>DWELLING</strong> of <strong>ANOTHER</strong>. <strong>Malice</strong> for burning means <strong>wrongful intent</strong>. But modernly Arson has been broadly extended to the malicious burning of almost any <strong>STRUCTURE</strong>.</p>
<p><strong>INTENT</strong><br>
Here A acted with <strong>INTENT</strong> to commit arson <strong>because</strong> he decide to &quot;set fire&quot; to the atheletic equipment shed. </p>
<p><strong>DWELLING</strong><br>
That was <strong>not dwelling</strong>, so he was not trying to commit common law arson. (<strong>No common law arson</strong>)</p>
<p><strong>STRUCTURE</strong><br>
But it is a STRUCTURE because it is a &quot;shed&quot;.</p>
<p><strong>MALICIOUS INTENT</strong><br>
He was acting with <strong>MALICIOUS INTENT</strong> because he had no legal right to do this.</p>
<p>So this is arson under the modern law. </p>
