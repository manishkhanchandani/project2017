<p><strong>Rule</strong></p>
<p>Under contract law, if the parties enter into a contract knowing that the<strong> failure of some condition</strong> beyond <strong>their control </strong>will <strong>deny one of the parties</strong> the <strong>benefit</strong> of the bargain, that <strong>condition is an implied material condition </strong>of the contract, the <strong>failure of which excuses both parties</strong> from the agreement .



When the purpose of the contract is frustrated, the performance of the contract may be excused.</p>
