<p><strong>Rule</strong></p>
<p>Under the<strong> MAILBOX RULES</strong> of contract law a rejection is <strong>effective upon receipt</strong> while an acceptance may be effective upon dispatch. An EXCEPTION is made if the <strong>OFFEROR CHANGES POSITION</strong> in reliance upon a communication of rejection, <strong>not knowing that an acceptance was dispatched</strong> prior to receipt of the rejection. In that case the rejection is effective upon receipt regardless of the fact an acceptance was also dispatched.</p>
