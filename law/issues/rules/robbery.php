<p><strong>Rule</strong></p>
<p>Under CRIMINAL LAW a ROBBERY is a <strong>larceny</strong> from a person by use of <strong>force</strong> or <strong>fear</strong> to overcome the <strong>will of the victim to resist</strong>.</p>
<hr />
<p><strong>ATTEMPTED ROBBERY?</strong></p>
<p>Under criminal law an ATTEMPTED ROBBERY is committed with there is a SUBSTANTIAL STEP taken toward committing a robbery. A robbery is a LARCENY, a trespassory taking of personal property with an intent to permanently deprive, from a person by force or fear overcoming the will of the victim to resist.</p>
<p>

Here Tom took a SUBSTANTIAL STEP toward completing the robbery because he went to the store “with a gun” and “burst” through the door. And his goal was the crime of robbery because he “wanted to rob” the store.
</p>
<p>
Therefore, Tom can be charged with attempted robbery.</p>
