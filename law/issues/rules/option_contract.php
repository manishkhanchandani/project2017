<p><strong>Rule</strong></p>
<p>An option contract is a separate agreement <strong>to hold an original offer open</strong> for a certain <strong>period of time</strong> in <strong>exchange for consideration</strong>.</p>
<hr />
<p><strong>Sample Example 1</strong></p>
<p>Here, Delia signed a statement indicating that she would hold the offer open for
one month in exchange for Irv paying her $20. Delia will argue that this option
contract was not supported by consideration because Irv did not in fact pay the
$20 to her. However, courts have held that this is not a valid defense so long as
there is a recital that valid consideration has been paid within the statement
indicating that the offer will remain open.</p>
<p>Therefore, this is a valid option contract.</p>
