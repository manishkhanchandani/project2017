<p><strong>Rule</strong></p>
<p>Under contract law every valid contract, and <strong>any modification of a contract</strong>, must be supported by <strong>CONSIDERATION</strong>, a BARGAINED FOR exchange posing sufficient LEGAL DETRIMENT that the law will enforce the agreement.</p>
<hr />
<p><strong>MODIFICATION without CONSIDERATION?</strong></p>
<p>Under <strong>COMMON LAW</strong> a contract modification must be supported by <strong>CONSIDERATION</strong>, a bargained for exchange of value posing legal detriment sufficient to enforce the modification at law.</p>
<p>Here the <strong>contract was modified</strong> because Homer promised to pay $30,000 more. But Bill did not give anything of value in exchange for that promise since he only agreed to do what he already had a PRE-EXISTING DUTY to do -- to build the house for $100,000.</p>
<p>Therefore, the modification of the contract was not supported by consideration and it is unenforceable at law against Homer.</p>
