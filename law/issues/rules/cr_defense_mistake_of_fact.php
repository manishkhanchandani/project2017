<p><strong>Rule</strong></p>
<p>Under CRIMINAL LAW a <strong>MISTAKE OF FACT</strong> is a complete defense if it negates implied criminal intent. For <strong>GENERAL INTENT</strong> crimes only a <strong>REASONABLE</strong> mistake can negate criminal intent. For <strong>SPECIFIC INTENT</strong> crimes <strong>ANY MISTAKE OF FACT</strong> may negate criminal intent whether reasonable or not. Battery, rape, arson, involuntary manslaughter and murders that are not willful and deliberate are general intent crimes. All other crimes are <strong>SPECIFIC INTENT</strong> crimes.</p>
<p>A <strong>REASONABLE MISTAKE OF FACT</strong> is one that a reasonable person would have made in the same situation. <strong>VOLUNTARY INTOXICATION</strong> never makes an otherwise unreasonable mistake reasonable.</p>
<p>A MISTAKE OF FACT is no defense to a charge of <strong>ATTEMPT</strong> if criminal intent is proven and the mistake merely prevented an otherwise criminal act.</p>
