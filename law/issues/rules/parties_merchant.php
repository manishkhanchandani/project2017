<p>Under the UCC, a Merchant is a person who<strong> trades in</strong> or otherwise <strong>holds himself out </strong>by occupation or otherwise <strong>knowledgeable about the goods</strong> of the contract. Further, a <strong>principal</strong> that <strong>employs</strong> an <strong>agent</strong> that is a <strong>merchant</strong> is held to be a <strong>merchant by implication.</strong></p>
<hr />
<p><strong>Sample Example 1</strong></p>
<p>Under the UCC, a Merchant is a person who<strong> trades in</strong> or otherwise <strong>holds himself out </strong>by occupation or otherwise <strong>knowledgeable about the goods</strong> of the contract. </p>
<p>

Here Bob <strong>deals in tuxedos </strong>because he has a clothing store. Al may have "bought a lot of clothes " but there is no evidence he deals in tuxedos or hold himself out by occupation as being particularly knowledgeable about them.</p>
<p>

Therefore Bob is a <strong>merchant</strong> and Al is <strong>not</strong>. </p>
<hr />
<p><strong>Sample Example 2 </strong></p>
<p>Under the UCC, a Merchant is a person who<strong> trades in</strong> or otherwise <strong>holds himself out </strong>by occupation or otherwise <strong>knowledgeable about the goods</strong> of the contract. </p>
<p> Here Sellco is clearly a <strong>dealer in these types of goods </strong>because it advertises them in its &quot;catalogue.&quot; Buyco is by <strong>occupation knowledgeable</strong> concerning &quot;widgets&quot; because of the large quantities it is ordering ($60,000).</p>
<p> Therefore, the parties appear to be merchants. </p>
