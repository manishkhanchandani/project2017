<p><strong>Rule</strong></p>
<p>Under criminal law VOLUNTARY MANSLAUGHTER is an <strong>unlawful, intentional homicide</strong> without <strong>malice aforethought</strong> because of <strong>adequate provocation </strong>sufficient to raise a reasonable person to a fit of rage which could and actually did cause the homicide.</p>
<p>Generally &quot;mere words alone&quot; are not considered at law to be adequate provocation.</p>
<p><strong>Elements:</strong></p>
<ul>
    <li> defendant committed murder</li>
    <li>adequate provocation </li>
    <li>defendant lost self-control (subjective test) </li>
    <li>a reasonable person would lose self-control (objective test) </li>
    <li>no cool off period</li>
</ul>
<hr />
<p><strong>Analysis</strong><br />
Here there was a lack of adequate provocation because &quot;hard looks&quot;, being &quot;dissed&quot;, challenged to a &quot;spelling bee&quot; and criticisms of attire&quot; are &quot;mere words alone&quot; that would not be sufficient to raise a reasonable person to a fit of murderous rage. The subjective factor that Killer had a &quot;short fuse&quot; would not lower the standard of conduct.</p>
<p>Therefore, Killer would not succeed in reducing the murder charge to voluntary manslaughter.</p>
<p><strong>Counter analysis</strong></p>
<p>Here there was a <strong>adequate provocation</strong> because &quot;sleeping of wife with friend&quot; would be sufficient to raise a reasonable person to a fit of murderous rage. </p>
<p>Therefore, Killer would succeed in reducing the murder charge to voluntary manslaughter.</p>
