<p>Under Criminal law, an <strong>ATTEMPTED CRIME</strong> is a <strong>SUBSTANTIAL STEP</strong> taken toward committing an <strong>INTENDED CRIME</strong>.</p>
<p>Two things to prove: 1) <strong>intent</strong> to commit a stated crime; 2) <strong>substantial step</strong> to do it. </p>
<hr />
<p><strong>Attempted Arson Example</strong></p>
<p>Under criminal law, an attempted arson is a <strong>SUBSTANTIAL STEP</strong> taken with <strong>INTENT</strong> to commit arson. A <strong>substantial step</strong> is an <strong>ACT</strong> that brings the defendent <strong>DANGEROUSLY CLOSE</strong> to committing the intended crime. </p>
<p>Under common law, Arson is <strong>MALICIOUS BURNING</strong> of the <strong>DWELLING</strong> of <strong>ANOTHER</strong>. <strong>Malice</strong> for burning means <strong>wrongful intent</strong>. But modernly Arson has been broadly extended to the malicious burning of almost any <strong>STRUCTURE</strong>.</p>
<p><strong>INTENT</strong><br>
Here A acted with <strong>INTENT</strong> to commit arson <strong>because</strong> he decide to &quot;set fire&quot; to the atheletic equipment shed. </p>
<p><strong>DWELLING</strong><br>
That was <strong>not dwelling</strong>, so he was not trying to commit common law arson. (<strong>No common law arson</strong>)</p>
<p><strong>STRUCTURE</strong><br>
But it is a STRUCTURE because it is a &quot;shed&quot;.</p>
<p><strong>MALICIOUS INTENT</strong><br>
He was acting with <strong>MALICIOUS INTENT</strong> because he had no legal right to do this.</p>
<p>So this is arson under the modern law. </p>
<p>And A made a <strong>SUBSTANTIAL STEP</strong> toward committing his intended crime because he came <strong>DANGEROUSLY CLOSE</strong> because he had &quot;matches&quot; and made &quot;severe trees&quot; to set the shed on fire.</p>
<p>Therefore A could be charged with attempted arson. </p>
<hr />
<p><strong>Attempted Robbery Example</strong></p>
<p>Under criminal law an <strong>ATTEMPTED ROBBERY</strong> is the act of taking a <strong>SUBSTANTIAL STEP</strong> toward commission of a <strong>robbery</strong>. A robbery is a <strong>LARCENY</strong>, a <strong>trespassory taking of personal property</strong> with an <strong>intent to permanently deprive</strong>, from a person <strong>by force or fear overcoming the will of the victim to resist</strong>.</p>
<p>

Here Tom took a <strong>SUBSTANTIAL STEP</strong> toward completing the robbery because he went to the store "with a gun" and "burst" through the door. And his goal <strong>was the crime of robbery</strong> because he "wanted to rob" the store.</p>
<p>

Therefore, Tom can be charged with attempted robbery.</p>
