<p><strong>Rule</strong></p>
<p>Under the UCC a non-breaching seller can sell rejected but conforming goods at a <strong>PUBLIC</strong> OR <strong>PRIVATE SALVAGE SALE</strong> (with NOTICE TO BREACHING BUYER) and demand the excess, if any, of the <strong>CONTRACT PRICE</strong> over the <strong>SALVAGE SALE</strong> price.</p>
<p>

Alternatively, in a <strong>LOST-VOLUME SITUATION</strong> where sellers cannot effectively sell the same goods to someone else, they can demand the <strong>BENEFIT OF THE BARGAIN</strong> of their "lost profits" - the excess, if any, of the <strong>CONTRACT PRICE</strong> over their cost of acquiring the goods.</p>
<p>

And if the goods have been <strong>special made</strong> or for some other reason <strong>can not be sold elsewhere</strong> the non-breaching seller can "<strong>sue on the contract price</strong>" to get a judgment for that amount.</p>
