<p><strong>Rule</strong></p>
<p>Realize the<strong> first party to breach</strong> a contract<strong> is the breaching party</strong> and after that the <strong>other party is the non-breaching party</strong> no matter what they do later.</p>
<p>



For <strong>COMMON LAW</strong> say,</p>
<p>

Under contract law a BREACH is a <strong>failure to perform a contractual duty</strong> when it becomes due. A MAJOR BREACH is either an act which <strong>deprives</strong> the other party of the <strong>BENEFIT OF THE BARGAIN</strong> or the <strong>violation of an EXPRESS OR IMPLIED MATERIAL CONDITION</strong> by a party, regardless of its effect on the other party. A major breach <strong>EXCUSES </strong>the non-breaching party from <strong>all further performance </strong>of contractual duties and <strong>ACCELERATES</strong> the future contractual duties of the breaching party <strong>to the present </strong>so the non-breaching party can seek<strong> IMMEDIATE PAYMENT OF DAMAGES</strong>.</p>
<p>


For <strong>UCC</strong> say,</p>
<p>

Under the <strong>PERFECT TENDER RULE</strong> of the UCC <strong>any shipment of non-conforming goods</strong> is a breach of contract. There is no distinction between major and minor breaches.</p>
<p>


For <strong>UCC DIVISIBLE CONTRACT SITUATION</strong> say,</p>
<p>

Under the UCC a breach with respect to<strong> any shipment of goods under a DIVISIBLE CONTRACT does not constitute a breach of the entire contract.</strong> A DIVISIBLE contract is one under which goods are to be delivered in separate shipments which can each be evaluated separately under the contract.</p>
