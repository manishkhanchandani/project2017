<p><strong>Rule</strong></p>
<p>Under the common law<strong> MIRROR IMAGE RULE</strong> an acceptance is an <strong>unequivocal assent</strong> to an offer.</p>
<p>Under <strong>UCC 2-206</strong> an acceptance of an offer <strong>not otherwise expressly conditioned</strong> may be <strong>made in any REASONABLE MANNER</strong>, including a promise to ship or shipment of either conforming or non-conforming goods. BUT a shipment of <strong>NON-CONFORMING</strong> goods as an <strong>EXPRESS ACCOMMODATION</strong> is not an acceptance. Further, <strong>UCC 2-207</strong> allows an <strong>acceptance containing varying terms</strong> to be effective.</p>
<hr />
<p><strong>Sample Example 1 (Did Homer effectively ACCEPT at 3:00?)</strong> </p>
<p>At common law an acceptance must be an unequivocal assent to the terms of an offer, dispatched to the offeror in the manner required, or if no manner is required in the offer, by the means by which the offer was dispatched or some faster means.. An offer must be accepted before it LAPSES. Unless there is an option, an offer lapses in the time stated or if no time is stated within a reasonable time after the offer is made.</p>
<p>Here the reasonable time for acceptance of Lucy's offer is defined by the understanding that Homer would view the house in only one hour at "10:00". That understanding established the "reasonable time" within which Homer could accept the offer. But Homer was "five hours late," so Lucy's offer had already lapsed when he arrived. Homer's attempt to unequivocally accept Lucy's 9:00 offer by saying "I unequivocally accept" was ineffective because he could not accept an offer that had already lapsed.</p>
<p>Therefore, Homer's attempt to accept was ineffective.</p>
<hr />
<p><strong>Sample Example 2 (Did Al ACCEPT Bob's offer?)</strong> </p>
<p>Under the UCC acceptance of an offer can be made by any reasonable means showing assent to an offer.</p>
<p>Here Al showed assent by a <strong>reasonable means</strong> because he " agreed". </p>
<p>Therefore, Al <strong>accepted</strong> Bob's offer.</p>
<hr />
<p><strong>Sample Example 3 (6/3/99 ACCEPTANCE?)</strong> </p>
<p>Under UCC 2-206 an offer that does not clearly require acceptance in a specific manner can be accepted in any reasonable manner including a promise to ship or shipment of conforming or non- conforming goods.</p>
<p>

Under UCC 2-207 an acceptance that includes varying terms is still effective. If the contract is between merchants the varying term proposed by the offeree may become part of the contract if the offer did not expressly require acceptance according to its terms, the offeror does not object to the varying terms within a reasonable time, and the varying terms are not a material alteration of the offer terms.</p>
<p>

Even when varying terms do not automatically become part of the contract they may become part of the contract by agreement, and the UCC allows modification without additional consideration.</p>
<p>
Here Sellco did not promise to ship in response to Buyco's offer because it said it "could provide" and not that it "would" provide the product. The word "could" does not imply any "promise to ship". Therefore the response of Sellco was a rejection of Buyco's offer and and a counter-offer to sell on different terms.
</p>
<p>


Buyco's reaction to Sellco's offer to sell without warranty was an acceptance because Buyco "verbally agreed."</p>
<p>

Even if one argues that Sellco accepted Buyco's offer with a varying term, Buyco still "agreed" to it, so that would have modified the contract to exclude the warranty term anyway.</p>
<p>

Therefore, there was an acceptance under either interpretation, and there is "no warranty" under either interpretation.</p>
<hr />
<p><strong>Sample Answer</strong></p>
<p>Here A showed assent by a <strong>reasonable means</strong> because he &quot; agreed&quot;. </p>
<p>Therefore A accepted B's offer. </p>
