<p><strong>Rule</strong></p>
<p>Under contract law the parties may agree at the time of contract execution that <strong>timely performance</strong> (by the party to first perform contract services) is a <strong>MATERIAL CONDITION</strong>. Then the condition is called an <strong>EXPRESS CONDITION</strong>, and typically the contract says, &quot; <strong>Time is of the essence</strong>&quot;.</p>
<p>


But material conditions may also be or implied when the parties knew at the time of execution that<strong> tardy performance</strong> would <strong>deny</strong> the non-performing party the <strong>BENEFIT OF THE BARGAIN</strong>, the benefit expected when they entered into the agreement. In that case the condition is called an <strong>IMPLIED MATERIAL CONDITION</strong>.</p>
