<p><strong>Rule</strong></p>
<p>Under contract law if a party performs a contractual <strong>duty</strong> that is subject to a<strong> condition precedent</strong> at a time the <strong>condition fails to hold</strong>, the party <strong>WAIVES THE CONDITION</strong>. But after a <strong>condition has been waived </strong>the waiving party has a <strong>legal right to retract the waiver</strong>. Nevertheless, a Court of EQUITY may estop the retraction based on considerations of equitable estoppel.</p>
