<p><strong>Rule</strong></p>
<p>Under common law LARCENY was the trespassory taking and carrying away of the personal property of another with intent to permanently deprive.
</p>
<p>
Where the possession was gained by misrepresentation it was called LARCENY BY TRICK.</p>
<p>

Theft of property from a master or employer by a manager or high-level employee was generally embezzlement and not a larceny, unless the defendant got possession by misrepresentation, a larceny by trick. But theft of the same property by a servant or low-level employee was generally larceny, not embezzlement, unless the defendant took possession from a third party before deciding to steal it, and in that case it was embezzlement.
</p>
<p>


Under the RELATION BACK DOCTRINE some common law courts held that a theft of "lost" property by a person who initially intended to return it to the lawful owner was a larceny because a later decision to steal RELATED BACK to make the original taking unlawful. But other courts held this was embezzlement on the theory the original taking formed a "constructive trust."</p>
