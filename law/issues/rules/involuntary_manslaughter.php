<p><strong>Rule</strong></p>
<p>Under criminal law, Involuntary manslaughter is an <strong>unintentional homicide</strong> resulting from <strong>GROSS NEGLIGENCE</strong>, <strong>RECKLESSNESS</strong> or the commission of a <strong>MALUM IN SE</strong> crime insufficient to support the felony murder rule.</p>
<p>GROSS (CRIMINAL) NEGLIGENCE is the deliberate <strong>breach of a pre-existing duty to protect others</strong> from extreme risks, and RECKLESSNESS is the deliberate <strong>creation of extreme risks</strong> to others.<br />
</p>
