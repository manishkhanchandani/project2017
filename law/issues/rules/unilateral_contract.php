<p><strong>Rule</strong></p>
<p>Under the common law a <strong>UNILATERAL CONTRACT OFFER</strong> is one that <strong>unequivocally</strong> indicates <strong>acceptance</strong> can <strong>only</strong> be manifested <strong>by</strong> completion of <strong>performance</strong> by the offeree. GENERAL OFFERS, reward or bounty offers, are always unilateral contract offers.</p>
<hr />
<p><strong>Sample Example 1</strong></p>
<p>Under the common law a <strong>UNILATERAL CONTRACT OFFER</strong> is one that <strong>unequivocally</strong> indicates <strong>acceptance</strong> can <strong>only</strong> be manifested <strong>by</strong> completion of <strong>performance</strong> by the offeree.</p>
<p>Here this offer <strong>unequivocally required</strong> Dottie to &quot;actually take care of &quot; Fester for the rest of his life because Fester &quot;emphasized&quot; Dottie could not accept by merely &quot;promising&quot; to do what he asked.</p>
<p>

Therefore, this was an offer of a unilateral contract.</p>
<hr />
<p><strong>Sample Example 2 (SAVING DOCTRINES of a UNILATERAL CONTRACT?)</strong></p>
<p>Under the common law an offeror could revoke her offer at any time before acceptance. Under that view Fester could revoke at any time, leaving Dottie without a remedy. Modernly SAVING DOCTRINES have been adopted to prohibit the offeror of a unilateral contract from revoking for a reasonable time after becoming aware that the offeree has begun performance in acceptance.
</p>
<p>


Under an alternative view the offeror may revoke but the offeree has a right to RESTITUTION to avoid UNJUST ENRICHMENT.</p>
<p>



Here Dottie began performance in acceptance because she "sold her home", "moved" and "nursed Fester." And Fester was aware of her performance because he was alert despite his paralysis.</p>
<p>



Therefore, under the modern savings doctrines Dottie would usually be given a reasonable time to complete performance. Here a "reasonable time" is the rest of Fester's life, since that was the condition of the offer.</p>
