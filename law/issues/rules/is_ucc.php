<p><strong>Sample 3 Is UCC for personal property?</strong></p>
<p>UCC Article 2 governs the sale of goods, moveable things at the time of identification to the contract. But UCC Article 1 generally requires a written contract signed by the party to be bound for any sale of personal property that is not goods in excess of $5,000.</p>
<p>



Here Tom claimed the records were "goods" but the contract was <strong>not for the sale of anything movable</strong> because it was for the sale of "recording rights". But it was for a <strong>sale of personal property</strong> because it was for "exclusive recording rights." The value of the contract was not stated to be over $5,000, and it might be argued that on that <strong>basis Article I would not require a writing at the time of execution</strong>, even though the song later became popular.</p>
<p>


Therefore UCC <strong>Article 2 would not require this contract to be in writing</strong>, and it is arguable that <strong>Article 1 would not require a writing because the contract</strong> was not clearly in excess of $5,000 at the time of execution.</p>
