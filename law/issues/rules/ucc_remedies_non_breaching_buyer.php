<p><strong>Rule</strong></p>
<p>Under the <strong>PERFECT TENDER RULE</strong> of the UCC a non-breaching buyer can either <strong>ACCEPT</strong> or <strong>REJECT</strong> non-conforming goods. Also they can <strong>REPUDIATE</strong> the contract and COVER, or they can <strong>AFFIRM</strong> the contract and <strong>DEMAND CONFORMING GOODS</strong>. The measure of damages is the excess, if any, of <strong>market or cover price</strong> over the <strong>contract price</strong>. </p>
