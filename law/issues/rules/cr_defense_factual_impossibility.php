<p><strong>Rule</strong></p>
<p>Under CRIMINAL LAW, <strong>FACTUAL IMPOSSIBILITY</strong> is a defense that the <strong>act actually done</strong> by the defendant was <strong>NOT A SUBSTANTIAL STEP</strong> toward commission of any crime, despite <strong>criminal intent</strong>, because the <strong>act taken could never produce a criminal result</strong>.</p>
