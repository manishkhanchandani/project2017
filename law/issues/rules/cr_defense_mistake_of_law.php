<p><strong>Rule</strong></p>
<p>Under CRIMINAL LAW, A MISTAKE OF LAW about the legality of an act does not alter the legality of the act.</p>
<p>

If the defendant commits a criminal act believing it is legal, it is still an illegal act. Likewise, if the defendant commits a legal act believing it is illegal, it is still a legal act. </p>
