<p><strong>Rule</strong></p>
<p>Mutual assent to the terms of the contract indicates an<strong> offer posed by the offeror</strong> and <strong>accepted by the offeree</strong>. A valid contract must have mutual assent to the essential terms of the contract and consideration between the parties.</p>
<hr />
<p><strong>Sample Example 1</strong></p>
<p>Mutual assent to the terms of the contract indicates an<strong> offer posed by the offeror</strong> and <strong>accepted by the offeree</strong>. A valid contract must have mutual assent to the essential terms of the contract and consideration between the parties.</p>
<p>Here, Mary and Dan mutually assented to the terms that Dan would purchase the football for $10,000 on or before Dec. 1, 2016. Dan had <strong>offered</strong> Mary $10,000 for the ball and Mary <strong>wrote up an affirmation </strong>of the contract on the napkin which Dan signed and Mary kept. This shows the<strong> intent of both parties to enter into a contract</strong> for the sale of the collectable football.</p>
<p>

There was <strong>mutual assent </strong>to the terms of the contract.</p>
