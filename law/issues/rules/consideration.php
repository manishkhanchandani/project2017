<p><strong>Rule</strong></p>
<p><strong>CONSIDERATION</strong> is an <strong>exchange of promises</strong> posing <strong>legal detriment</strong>.</p>
<p>A detriment is refraining from doing something that one is legally entitled to do or doing something that one is not legally bound to do.

</p>
<hr />
<p><strong>Sample Example 1</strong></p>
<p>Jack made a <strong>unilateral contract </strong>offer to Justin, i.e. Justin will have to<strong> attend law school </strong>for atleast one semester in order<strong> to receive reimbursement of tuition</strong> for that semester.</p>
<p> 

Jack <strong>bargained</strong> for Justin's <strong>performance</strong>, <strong>attend law school, in exchange for his own promise</strong>, to reimburse tuition. Jack's offer induced current performance from Justin, because Justin had<strong> no preexisting duty</strong> to attend law school. </p>
<p>

Justin agreed to do so something which <strong>he was not legally obligated to do</strong> (attend law school), legal detriment. Jack bargained for Justin's performance and although the benefit is one of personal satisfaction,<strong> the court will not review beneficial motives</strong> for making a contract when analyzing consideration. </p>
<p>

Jack may claim that this contract is illusory because there is <strong>no mutuality of obligation</strong>, Justin was not obligated to perform while Jack would be bound by his promise for a reasonable time if Justin prepared or begun performance (Restatement 2nd Section 45 and 87). However, the <strong>principal of mutuality does not apply to unilateral contracts.</strong> There is valid consideration for Jack's contract with Justin. </p>
<hr />
<p><strong>Sample Answer</strong></p>
<p>Here A promised to do something because A was giving tution fee, Here A had preexisting duty to do something, so its consideration is not valid. </p>
<p>Here B promised to to in return because B was returning the work by performance in going to law school. B had no preexisting duty to attend the school, so its consideration is valid. </p>
