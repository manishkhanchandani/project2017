<p><strong>Rule</strong></p>
<p>Under <strong>COMMON LAW</strong> a <strong>BURGLARY</strong> was the <strong>breaking</strong> and <strong>entering</strong> of the <strong>dwelling</strong> of <strong>another</strong> in the <strong>night</strong> with <strong>intent</strong> to commit a felony.</p>
<p>



Further, a <strong>CONSTRUCTIVE BREAKING</strong> would be found if entry was made by trick, threat of violence, or with the help of conspirator.</p>
<p>



MODERNLY burglary has been extended to almost<strong> any structure</strong> and <strong>all times</strong> of the day. If a larceny is not felony, it still supports a charge of burglary. And modernly a constructive breaking will be found if there is any <strong>TRESPASSORY ENTRY</strong>, an entry without permission, express or implied.</p>
