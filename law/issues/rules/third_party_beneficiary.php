<p><strong>Rule</strong></p>
<p>Under contract law, A <strong>THIRD PARTY BENEFICIARY</strong> contract is a legally enforced contract entered by <strong>two parties</strong> for the <strong>intended purpose</strong> of <strong>benefiting</strong> a third party.</p>
<p>An <strong>INCIDENTAL</strong> beneficiary, one that was <strong>not intended</strong> to benefit from a contract between others, has <strong>no ability</strong> to enforce the contract or seek damages.</p>
<p>

<strong>Intended Third party beneficiary</strong> has a legal right to enforce the contract against the promisors. This is called "<strong>standing</strong>". In some jurisdictions, intended third party beneficiary must be "<strong>vested</strong>", explained below.</p>
<p><strong>

Creditor and Donee Beneficiaries</strong></p>
<p>

If promisees enters into third-party beneficiary contracts to <strong>satisfy legal obligations</strong>, the intended third party beneficiaries have a <strong>legal right</strong> to enforce the contracts against the promisees. In this case the intended third-party beneficiaries are called "creditor beneficiaries".</p>
<p>
If promisees enters into third-party beneficiary contracts for <strong>gratuitous purposes,</strong> the intended third-party beneficiaries have <strong>NO legal right</strong> to enforce the contracts against the promisees. In this case intended third party beneficiaries are called "donee beneficiaries".</p>
<p>
<strong>Intended third-party beneficiaries</strong> always have <strong>standing</strong> to enforce third-party beneficiary contracts against <strong>promisors</strong>, whether they are donee or creditor beneficiaries.</p>
<p><strong>

Defenses</strong></p>
<p>

The promisors can raise any <strong>defense</strong> against the intended third party beneficiaries that could have been raised against the promisees.</p>
<p>
The promisees can raise any <strong>defense</strong> against the intended third party beneficiaries that could have been raised against the promisors.</p>
<p><strong>
Standing</strong></p>
<p>

Parties seeking a remedy must have a<strong> legal right </strong>to enforce the contract. This is called <strong>standing</strong>. Original parties always have standing towards each other. In case of third party beneficiary, third party do not have standing unless they are "<strong>vested</strong>" (explained below). Incidental beneficiaries do not have standing and so they cannot enforce the contract.</p>
<p><strong>Vesting</strong></p>
<p>

Before third-parties can enforce third-party beneficiary contracts they must be "vested". To be "vested", third-party beneficiary must "act in reliance" on the existence of the third party beneficiary contract.</p>
