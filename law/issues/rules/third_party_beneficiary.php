<p><strong>Groucho's rights under a THIRD-PARTY BENEFICIARY CONTRACT?</strong></p>
<p>Under contract law a THIRD-PARTY BENEFICIARY CONTRACT is one with a main purpose of providing a benefit to a third party. The contract can be enforced by the original party, the promisee, or by a vested intended beneficiary.</p>
<p>

A beneficiary may be a DONEE (of a gift) or a CREDITOR (payment of a debt) beneficiary. The beneficiary vests upon becoming aware of and changing position in reliance on the contract.</p>
<p>

Here the purpose of the contract is to benefit Chico because he was to get a "new Dodge" as a "birthday present." Therefore this is a third-party benefit contract. Chico is an intended DONEE beneficiary because it is a "birthday present."</p>
<p>

Groucho is the promisee because Harpo promised to "deliver" the car in "two weeks." Harpo is the promisor because he has promised to deliver the car to Chico.</p>
<p>

Therefore, this is a third-party beneficiary contract, and Groucho as promisee can enforce it.</p>
<p>

<strong>3) Groucho's REMEDIES?</strong></p>
<p>

Under the PERFECT TENDER RULE of the UCC a non-breaching buyer in a non-delivery situation can demand conforming goods, repudiate the contract or by goods to cover and demand the excess of the cover price over the contract price.</p>
<p>

Here Harpo has breached because he did not deliver the promised car.</p>
<p>

Therefore, Groucho can demand delivery from Harpo or repudiate the contract and demand return of his $2,000. If he covers by buying the car elsewhere he can demand the excess of his cover price, if any, over the contract price.</p>
