<p><strong>Chico's rights as a THIRD-PARTY BENEFICIARY?</strong></p>
<p>Third-party beneficiary contracts are defined above. Chico is an intended third-party beneficiary because the known purpose of the original contract was to give him a "birthday present." He vested when he became aware of the original contract and changed position in reliance.</p>
<p>

A vested third-party beneficiary can enforce the contract against the promisor in the same manner as the promisee.</p>
<p>

Therefore, Chico can enforce the original contract against Harpo in the same manner and extent as Groucho</p>
