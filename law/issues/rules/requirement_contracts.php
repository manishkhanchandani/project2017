<p><strong>Rule</strong></p>
<p><strong>Requirement Contracts</strong></p>
<p>

A requirements contract offer is one that offers to buy or sell "<strong>all needed amounts</strong>" by the buyer during a stated period of time.</p>
<p>

The "quantity" of the contract is the amount actually needed by the buyer during the stated period, but not more than reasonably proportionate to amounts previously needed. (UCC 2-306)</p>
<p>

A valid requirements contract offer must bind the buyer to purchase All amounts needed from the seller and cannot be just for amounts "ordered" or "wanted".
</p>
<p><strong>


OUTPUT contracts</strong></p>
<p>

An output contract offer is one that offers to buy or sell "<strong>all output</strong>" produced by the seller during a stated period of time.</p>
<p>

The "quantity" of the contract is the amount actually produced by the seller during the stated period, but not more than reasonably proportionate to amounts previously produced (UCC 2-306)</p>
