<p><strong>Rule</strong></p>
<p>An <strong>IMPLIED MATERIAL CONDITION</strong> of every contract is that the parties must act in <strong>good faith</strong> and are bound by an <strong>IMPLIED COVENANT</strong> that they will act as reasonably necessary<strong> to help the other parties</strong> enjoy the <strong>benefits of the contract </strong>and that they will not act in any way to prevent that from occurring.</p>
