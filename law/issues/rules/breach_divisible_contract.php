<p><strong>Rule</strong></p>
<p>Under the UCC a<strong> breach with respect to any shipment of goods</strong> under a <strong>DIVISIBLE CONTRACT</strong> does <strong>not</strong> constitute a <strong>breach of the entire contract</strong>. A DIVISIBLE contract is one under which <strong>goods are to be delivered in separate shipments </strong>which can <strong>each be evaluated separately</strong> under the contract.</p>
