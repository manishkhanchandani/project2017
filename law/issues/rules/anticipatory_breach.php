<p><strong>Rule</strong></p>
<p>Under contract law an anticipatory breach (or anticipatory repudiation) is a <strong>CLEAR STATEMENT OR INDICATION</strong> that a party <strong>WILL NOT PERFORM</strong> future contractual duties when they become due. This is a major breach as to those duties, the non-breaching party is <strong>EXCUSED</strong> from performance of all remaining contractual duties, and the future duties of the breaching party are <strong>ACCELERATED</strong> to the present.</p>
<p>


If a party has a <strong>REASONABLE BASIS</strong> to believe the other party may not perform future contractual duties, the party may demand <strong>REASONABLE ASSURANCES</strong> and <strong>REFUSE TO PERFORM </strong>until they are provided. Reasonable assurances generally means a financial guarantee or payment into escrow.</p>
<p>
If reasonable assurances are properly demanded and not provided, the failure to respond constitutes an anticipatory breach.</p>
