<p><strong>Rule</strong></p>
<p>Under CRIMINAL LAW consent is a defense to some crimes.[rape, larceny, battery – consent to a touching] The consent must be informed, voluntary and given by one with legal capacity. Further, consent is not a defense to an act that causes great bodily harm.</p>
