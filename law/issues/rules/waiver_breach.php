<p><strong>Rule</strong></p>
<p>Under contract law if a <strong>non-breaching party lets the breaching party continue performance</strong> after a <strong>major breach</strong> it <strong>WAIVES THE BREACH</strong>. The waiver cannot be revoked, and the non-breaching party is forever ESTOPPED from citing the breach as major and can only cite it as a minor breach.</p>
