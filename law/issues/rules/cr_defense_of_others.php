<p><strong>Rule</strong></p>
<p>Under CRIMINAL LAW a defendant is privileged to act with <strong>reasonable forc</strong>e to <strong>protect</strong> <strong>others</strong> from an aggressor. Courts are split when a defendant mistakenly aids an aggressor. Some Courts say the defendant who mistakenly aids an aggressor STEPS-INTO-THE-SHOES of the aggressor and cannot claim any defense. Other Courts hold that a defendant can claim this defense if they acted on REASONABLE APPEARANCES.</p>
