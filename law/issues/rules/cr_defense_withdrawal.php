<p>Under <strong>CRIMINAL LAW</strong>, <strong>WITHDRAWAL</strong> is a defense that defendants who were members of a <strong>CONSPIRACY</strong> are not liable for crimes committed by co-conspirators AFTER the defendants</p>
<p>

1) give the other co-conspirators <strong>NOTICE</strong> that they are abandoning the conspiracy and</p>
<p>

2) the defendants <strong>TRY TO STOP</strong> the co-conspirators from continuing pursuing the conspiracy goal.</p>
