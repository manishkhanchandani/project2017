<p><strong>Rule</strong></p>
<p>Under common law EMBEZZLEMENT was the crime of <strong>TRESPASSORY CONVERSION</strong> of the property of another by <strong>one entrusted with lawful possession</strong> with <strong>intent to permanently deprive</strong> or causing substantial risk of loss.</p>
<p>

Further, the COMMON CARRIER DOCTRINE held that a common carrier such as a taxicab, bus or ship is entrusted with possession of passenger’s property, including lost property, so a theft of passenger property by an employee of a common carrier was more often held to be EMBEZZLEMENT than larceny, even if the property was "lost".</p>
