<p>Under a Strict Liability theory, the plaintiff must show the seller was <strong>COMMERCIAL SUPPLIER</strong>, the product was<strong> unreasonably dangerous</strong> at the time it <strong>left the defendant's control</strong>, buyer is <strong>injured</strong>, and defendant is only liable for <strong>non-economic damages</strong>. 
    
    </p>
<p>The seller is liable to any person who is injured as a result.</p>
