<p><strong>Rule</strong></p>
<p>Under PEERLESS when both parties enter into a contract because of a <strong>misunderstanding as to a material fact</strong>, there is <strong>no “meeting of the minds”</strong> and the contract is <strong>void</strong> from the beginning.</p>
<p>



A mutual mistake can void the contract if both parties are<strong> honestly mistaken about a material fact</strong> of the contract.</p>
