<p><strong>Rule</strong></p>
<p>Under contract law reasonably satisfactory performance by the parties is ALWAYS AN IMPLIED MATERIAL CONDITON of every contract. However, the parties may agree that buyer <strong>SATISFACTION</strong> is a <strong>MATERIAL CONDITION</strong> so buyers who are <strong>not actually satisfied</strong> have <strong>no legal duty to pay</strong>.</p>
