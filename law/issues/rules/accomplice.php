<p><strong>Accomplice?</strong></p>
<p>A person who commits or helps commit a crime is liable for all subsequent crimes by co-felons that are a direct and natural result of the prior crimes.</p>
<p>

Here Dick helped Tom to rob the store the prior day, so he would be liable for any crimes by Tom that are a direct and natural result of the attempted robbery. But the murder of Victor was not a foreseeable result of the prior robbery attempt.</p>
<p>

Therefore Dick cannot be charged with Victor's death based on accomplice liability.</p>
<p>

Since Dick had no direct liability or vicarious liability he cannot be charged with Murder.</p>
