<p><strong>Rule</strong></p>
<p>Under the theory of <strong>DETRIMENTAL RELIANCE</strong> an agreement that otherwise is unenforceable at law for deliberately <strong>false representations of fact</strong> by the respondent OR deliberately <strong>misleading behavior</strong> of the respondent may be enforced at equity to the extent necessary to<strong> avoid injustice</strong>. In order to plead equity a party must have <strong>CLEAN HANDS</strong>. Detrimental Reliance applies where the party to be bound made a 1) <strong>promise</strong> with 2) intent to<strong> induce reliance</strong> and there 3) was <strong>reasonable reliance</strong> and 4)<strong> failure to enforce the promise</strong> would cause an injustice.</p>
