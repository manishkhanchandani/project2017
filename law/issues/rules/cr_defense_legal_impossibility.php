<p><strong>Rule</strong></p>
<p>Under CRIMINAL LAW,<strong> LEGAL IMPOSSIBILITY</strong> means that an attempted act is not an attempted crime, even if there was criminal intent, when the attempted crime is a legal impossibility at the time of the first substantial step.</p>
