<p><strong>Rule</strong></p>
<p>LIQUIDATED DAMAGES CLAUSES specify that the <strong>sole remedy</strong> of the non-breaching party is a<strong> specified amount</strong> of money damages.</p>
<p>

These clauses are only enforceable if 1) <strong>DAMAGES</strong> arising from a breach were <strong>UNCERTAIN</strong> at the <strong>TIME OF CONTRACT</strong>, 2) the specified amount was <strong>REASONABLE</strong> at the time of contract, and 3) enforcement after a breach will provide a <strong>REASONABLE REMEDY</strong> for the non-breaching party.</p>
