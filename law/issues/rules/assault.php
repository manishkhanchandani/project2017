<p><strong>Rule</strong></p>

<p>Under tort law, An <strong>ASSAULT</strong> is an <strong>intentional overt act</strong> done to cause and that does causes  an<strong> immediate apprehension</strong> of a battery, a <strong>harmful</strong> or <strong>offensive</strong> <strong>contact</strong>. </p>
<p><strong>Analysis</strong></p>
<p>Here D acted <strong>intentionally</strong> because when the <strong>D</strong> shouted to P to "Facts from Hypo ," his conduct was volitional and therefore intentional.</p>
<p>
Here D's intent was to cause <strong>apprehension</strong> because of the words "Facts from Hypo ", Mere words are not adequate to cause apprehension, but D's <strong>words</strong> coupled with the fact that he had the present <strong>ability</strong> to carry the threat out indicates the placing of another in reasonable apprehension. </p>

<p>

Here D wanted to <strong>touch harmfully or offensively</strong> because he said "Facts from hypo ", these words show that D wanted to touch harmfully to the P.</p>
