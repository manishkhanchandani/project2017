<p><strong>Rule</strong></p>
<p>Under the UCC a breaching seller that <strong>GIVES NOTIC</strong>E of an intent to cure has an absolute right to cure the breach <strong>WITHIN THE CONTRACT PERIOD</strong>, and also has a right to <strong>REASONABLE EXTRA TIME</strong> in which to cure beyond the contract period if <strong>non-conforming goods</strong> have been shipped with a<strong> reasonable belief</strong> they would satisfy the needs of the buyer.</p>
