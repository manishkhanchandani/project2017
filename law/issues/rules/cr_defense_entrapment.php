<p><strong>Rule</strong></p>
<p>Under CRIMINAL LAW entrapment is a defense if criminal intent was the product of improper police behavior. </p>
<p>



Under the majority view entrapment is no defense if the defendant was predisposed to commit the crime. Under another minority view entrapment is a defense if police conduct was outrageous and instigated the crime, even though the defendant was predisposed.</p>
