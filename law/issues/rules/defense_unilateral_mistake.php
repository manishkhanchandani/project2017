<p><strong>Rule</strong></p>
<p>Under contract law, when one party enters into a contract because of a <strong>misunderstanding as to a material fact</strong> the <strong>majority view</strong> is that the parties are <strong>legally bound</strong> to the contract unless <strong>the other party knew</strong> or should have known <strong>of the mistake</strong>, and in that case the contract is voidable by the mistaken party.</p>
<p>

Under a minority view the contract is legally voidable by the mistaken party in any event if they 1) <strong>discover the mistake quickly</strong> before the other party substantially relies on the contract, 2) <strong>give prompt notice</strong> of the mistake, and 3) <strong>reimburse the other party</strong> for any expenses caused by the mistake.</p>
