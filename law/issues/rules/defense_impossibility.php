<p><strong>Rule</strong></p>
<p>Under contract law a material condition of every contract is that <strong>performance of contract duties must be objectively possible</strong> to perform. The inability of a contract party to perform is irrelevant, as long as performance, in general, is possible. But<strong> if performance becomes impossible </strong>because of <strong>events beyond the control </strong>of the parties the contract becomes void.</p>
