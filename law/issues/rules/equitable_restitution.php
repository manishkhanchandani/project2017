<p><strong>Rule</strong></p>
<p>EQUITABLE RESTITUTION is a remedy awarded by a Court of EQUITY when the parties have <strong>no adequate legal remedy</strong>. The purpose may be to compensate for <strong>injuries suffered</strong>, <strong>prevent unjust enrichment</strong>, prevent <strong>frustration of reasonable expectations</strong>, or restore the <strong>status quo</strong>.</p>
<hr />
<p><strong> What are Dottie's remedies in alternative to enforcement? (EQUITABLE RESTITUTION?)

</strong></p>
<p>At equity a party that cannot enforce a promise at law or equity may still seek damages in RESTITUTION to prevent UNJUST ENRICHMENT. The measure of damages may be either the benefits their performance conveyed to the breaching party, or else the detriment they have suffered in reasonable reliance.</p>
<p>

Here Dottie can seek RESTITUTION for the benefits she conveyed to Fester. He enjoyed benefits that would have cost him $40,000 a year for ten years, so he enjoyed an unjust enrichment of $400,000.</p>
<p>

In the alternative Dottie suffered a loss of $50,000 on her house and foregone wages of $2,500,000 over the ten year period, for a total of $2,550,000.
</p>
<p>


Dottie may seek an award of money damages in these amounts in equitable restitution.</p>
