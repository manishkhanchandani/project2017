<p><strong>Rule</strong></p>
<p>Under common law the non-breaching party has a right to <strong>COMPENSATORY DAMAGES</strong> calculated as the sum of 1) RELIANCE DAMAGES, <strong>out-of-pocket expenses</strong> the non-breaching party paid before the breach <strong>in reliance on the contract</strong>, 2) EXPECTATION DAMAGES, the <strong>expected benefits</strong> of the contract lost because of the breach, 3) INCIDENTAL DAMAGES, <strong>out- of-pocket expenses</strong> the non-breaching party paid <strong>because of the breach</strong> (e.g. storage costs) and 4) CONSEQUENTIAL DAMAGES, lost profits on collateral contracts that failed because of the breach.</p>
<p>

Under <strong>Hadley v. Baxendale</strong> CONSEQUENTIAL DAMAGES can only be awarded if, at the time the original contract was executed, the breaching party <strong>knew</strong> of or otherwise could have contemplated the non-breaching party would <strong>enter into collateral contracts</strong> and <strong>would lose profits</strong> on them as a result of a contract breach.</p>
<p>

The burden is on a non-breaching party to prove the damages suffered with <strong>CERTAINTY</strong>, to prove they were <strong>CAUSED</strong> BY THE BREACH, and that they <strong>COULD NOT BE AVOIDED </strong>after the breach occurred. By implication this gives the non-breaching party a duty to mitigate damages.</p>
<p>
If the breaching party has <strong>SUBSTANTIALLY PERFORMED</strong>, the non-breaching party is still obligated under the contract with an <strong>OFFSET</strong> for damages against the contract price. But if the breaching party is in <strong>MAJOR BREACH</strong> the non-breaching party is freed from all obligations under the contract and has a right to an award of all damages caused.</p>
