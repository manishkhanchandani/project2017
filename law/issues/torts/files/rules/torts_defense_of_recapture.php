<p>Under tort law a person has a qualified privilege to use <strong>reasonable</strong> force to <strong>RECAPTURE</strong> their own chattel if    </p>
<p>1) they have <strong>asked</strong> for and have been <strong>refused</strong> return of the chattel, and    </p>
<p>2) they are in<strong> fresh pursuit</strong> of wrongfully taken chattel, or    </p>
<p>3) they lost possession of the chattel through <strong>no fault</strong> of their own.</p>
